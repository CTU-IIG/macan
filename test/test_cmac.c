#include <stdint.h>
#include <nettle/aes.h>
#include <nettle/memxor.h>
#include "aes_cmac.h"
#include "common.h"

void test()
{
	struct aes_ctx cipher;
	uint8_t key[16] = {
		0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6,
		0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c
	};
	unsigned char M[64] = {
		0x6b, 0xc1, 0xbe, 0xe2, 0x2e, 0x40, 0x9f, 0x96,
		0xe9, 0x3d, 0x7e, 0x11, 0x73, 0x93, 0x17, 0x2a,
		0xae, 0x2d, 0x8a, 0x57, 0x1e, 0x03, 0xac, 0x9c,
		0x9e, 0xb7, 0x6f, 0xac, 0x45, 0xaf, 0x8e, 0x51,
		0x30, 0xc8, 0x1c, 0x46, 0xa3, 0x5c, 0xe4, 0x11,
		0xe5, 0xfb, 0xc1, 0x19, 0x1a, 0x0a, 0x52, 0xef,
		0xf6, 0x9f, 0x24, 0x45, 0xdf, 0x4f, 0x9b, 0x17,
		0xad, 0x2b, 0x41, 0x7b, 0xe6, 0x6c, 0x37, 0x10
	};
	uint8_t key1[16];
	uint8_t key2[16];
	uint8_t signature[16];
	unsigned len = 16;

	aes_set_encrypt_key(&cipher, len, key);

	/* key1 */
	uint8_t key1_chck[16] = { 0xfb, 0xee, 0xd6, 0x18, 0x35, 0x71, 0x33, 0x66,
		                  0x7c, 0x85, 0xe0, 0x8f, 0x72, 0x36, 0xa8, 0xde };
	generate_subkey(key, key1, key2);
	print_hex(key1);
	eval("subkey1", check128(key1, key1_chck));

	/* key 2 */
	uint8_t key2_chck[16] = { 0xf7, 0xdd, 0xac, 0x30, 0x6a, 0xe2, 0x66, 0xcc,
		                  0xf9, 0x0b, 0xc1, 0x1e, 0xe4, 0x6d, 0x51, 0x3b };
	print_hex(key2);
	eval("subkey2", check128(key2, key2_chck));

	/* sign0 */
	uint8_t sign0_chck[16] = { 0xbb, 0x1d, 0x69, 0x29, 0xe9, 0x59, 0x37, 0x28,
		                   0x7f, 0xa3, 0x7d, 0x12, 0x9b, 0x75, 0x67, 0x46 };
	aes_cmac(&cipher, 0, signature, M);
	print_hex(signature);
	eval("sign 0 bytes", check128(signature, sign0_chck));

	/* sign16 */
	uint8_t sign16_chck[16] = { 0x07, 0x0a, 0x16, 0xb4, 0x6b, 0x4d, 0x41, 0x44,
		                    0xf7, 0x9b, 0xdd, 0x9d, 0xd0, 0x4a, 0x28, 0x7c };
	aes_cmac(&cipher, 16, signature, M);
	print_hex(signature);
	eval("sign 16 bytes", check128(signature, sign16_chck));

	/* sign40 */
	uint8_t sign40_chck[16] = { 0xdf, 0xa6, 0x67, 0x47, 0xde, 0x9a, 0xe6, 0x30,
		                    0x30, 0xca, 0x32, 0x61, 0x14, 0x97, 0xc8, 0x27 };
	aes_cmac(&cipher, 40, signature, M);
	print_hex(signature);
	eval("sign 40 bytes", check128(signature, sign40_chck));

	/* sign64 */
	uint8_t sign64_chck[16] = { 0x51, 0xf0, 0xbe, 0xbf, 0x7e, 0x3b, 0x9d, 0x92,
		                    0xfc, 0x49, 0x74, 0x17, 0x79, 0x36, 0x3c, 0xfe };
	aes_cmac(&cipher, 64, signature, M);
	print_hex(signature);
	eval("sign 64 bytes", check128(signature, sign64_chck));
}

int main(int argc, char *argv[])
{
	test();

	return 0;
}

