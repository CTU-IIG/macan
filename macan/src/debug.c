#include <debug.h>
#include <stdio.h>
#include <stdarg.h>

void debug_printf(const char* format, ...)
{
    if (msg)
    {
        va_list argp;
        va_start(argp, msg);
        vfprintf(stderr, format, argp);
        va_end(argp);
    }
}
